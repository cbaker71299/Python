# Write a program that displays the area and perimeter of a circle that has a radius of 5.5 using the following formulas
# area = radius * radius * 3.14159
# perimeter = 2 * radius * 3.14159

radius = 5.5
area = 3.14159 * radius * radius
perimeter = 2 * radius * 3.14159

print("The area of a circle with a radius of " + str(radius) + " is " + str(area) + ".")
print("The perimeter of a circle with a radius of " + str(radius) + " is " + str(perimeter) + ".")





