# Write a program that displays the area and perimeter of a rectangle with the width of 4.5 and height of 7.9 using the following formula:
# area = width * height


width = 4.5
height = 7.9
perimeter = width * 2 + height * 2

area = width * height

print("The area of the rectangle with a width of 4.5 and height of 7.9 is " + str(area) + ".")
print("The perimeter of of a rectangle with a width of 4.5 and height of 7.9 is " + str(perimeter) + ".")
